// Helper function to get contracts based on the network
function getContractsForNetwork(network: string) {
  switch (network) {
    case 'sepolia':
      return SEPOLIA_CONTRACTS;
    case 'alfajores':
      return ALFAJORES_CONTRACTS;
    case 'celo':
      return MAINNET_CONTRACTS;
    default:
      // Default to Sepolia for development
      return SEPOLIA_CONTRACTS;
  }
}

